<h2> About Me </h2>

<img class="headshot" src="images/headshot.jpg" alt="A photo of me, Olivia Miller" width="250" height="280">

<p>I'm a 23 year old library and information science student with an interest in accessbility, user experience, and user research. I'm passionate about exploring how people with marginalized identities form user communities for their unique information needs. A Bay Area native, I graduated from UC Santa Barbara in 2014 with a Bachelors Degree in English and expect to graduate from San Jose State University in December 2016 with a Masters in Library and Information Science. </p>
<p> In my spare time I enjoy reading comics and graphic novels, creating seasonal mixtapes, and exploring every kind of breakfast food. My productivity is fueled by a combination of pop culture podcasts and matcha shots. At any given time I can be found at an assortment of coffee shops up and down the Bay Area. </p>

